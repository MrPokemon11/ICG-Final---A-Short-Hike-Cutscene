	private static Dictionary<string, FieldInfo> fields;

	public static Dictionary<string, FieldInfo> GetFields()
	{
		if (fields == null)
		{
			fields = new Dictionary<string, FieldInfo>();
			FieldInfo[] array = typeof(I18nStrings).GetFields();
			foreach (FieldInfo fieldInfo in array)
			{
				if (!fieldInfo.IsStatic)
				{
					fields.Add(fieldInfo.Name, fieldInfo);
				}
			}
		}
		return fields;
	}

	public static bool UpdateString(string key, string value)
	{
		Dictionary<string, FieldInfo> dictionary = GetFields();
		if (dictionary.ContainsKey(key))
		{
			dictionary[key].SetValue(I18n.STRINGS, value);
			return true;
		}
		return false;
	}
